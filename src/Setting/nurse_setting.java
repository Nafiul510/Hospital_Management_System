    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        conn = (com.mysql.jdbc.Connection) MySqlConnect.ConnectDB();
        if(conn != null){
            String userName = userName_txt.getText();
            String password = password_txt.getText();
            String sql = "INSERT INTO `log_in` (`UserName`, `Password`) VALUES (?, ?)";
            try{
                pst = conn.prepareStatement(sql);
                pst.setString(1, userName);
                pst.setString(2, password);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Saved Successfully!");

            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        clear();
        update_table();
        doctor_setting_table.setAutoResizeMode(doctor_setting_table.AUTO_RESIZE_OFF);
        doctor_setting_table.getColumnModel().getColumn(0).setPreferredWidth(266);
        doctor_setting_table.getColumnModel().getColumn(1).setPreferredWidth(266);
    }//GEN-LAST:event_save_btnActionPerformed

