    private void doctor_setting_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_doctor_setting_tableMouseClicked
        try{
            conn = (com.mysql.jdbc.Connection) MySqlConnect.ConnectDB();
            int row = doctor_setting_table.getSelectedRow();
            String tableClick = (doctor_setting_table.getModel().getValueAt(row, 0).toString());
            String sql = "SELECT * FROM log_in WHERE Password = '" + tableClick + "' ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                String userName = rs.getString("UserName");
                userName_txt1.setText(userName);
                String password = rs.getString("Password");
                password_txt1.setText(password);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_doctor_setting_tableMouseClicked

